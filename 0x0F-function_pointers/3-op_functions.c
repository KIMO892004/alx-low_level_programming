#include "3-calc.h"

/**
 * op_add - adds two integers numbers
 * @k: the first int
 * @r: the second int
 *
 * Return: the sum
 */
int op_add(int k, int r)
{
	return (k + r);
}

/**
 * op_sub - subtracts two integers numbers
 * @k: the first int num
 * @r: the second int num
 *
 * Return: the difference
 */
int op_sub(int k, int r)
{
	return (k - r);
}

/**
 * op_mul - multiplies two integers numbers
 * @k: the first int num
 * @r: the second int num
 *
 * Return: the product
 */
int op_mul(int k, int r)
{
	return (k * r);
}

/**
 * op_div - divides two integers numbers
 * @k: the first int num
 * @r: the second int num
 *
 * Return: the quotient
 */
int op_div(int k, int r)
{
	return (k / r);
}

/**
 * op_mod -  modulo two integers numbers
 * @k: the first int num
 * @r: the second int num
 *
 * Return: the remainder
 */
int op_mod(int k, int r)
{
	return (k % r);
}
